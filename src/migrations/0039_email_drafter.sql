-- 4. email_drafts — executor target (one row per approved draft_email action)
create table email_drafts (
  id                  uuid primary key default gen_random_uuid(),
  org_id              uuid not null references organizations(id) on delete cascade,
  payload_id          uuid not null references inbound_payloads(id) on delete cascade,
  proposed_action_id  uuid references proposed_actions(id),
  subject             text not null,
  body                text not null,
  recipient_type      text not null,
  tone                text not null,
  key_points          jsonb not null,
  created_at          timestamptz not null default now()
);
create index idx_email_drafts_org_id on email_drafts(org_id);
create index idx_email_drafts_payload_id on email_drafts(payload_id);

alter table email_drafts enable row level security;
create policy tenant_isolation_email_drafts on email_drafts
  using (org_id = current_setting('app.current_org_id', true)::uuid)
  with check (org_id = current_setting('app.current_org_id', true)::uuid);
